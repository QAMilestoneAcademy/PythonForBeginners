# If you want to use text in Python, you have to use a string.
# A string is created by entering text between two single or double quotation marks.

print("Python is fun!")
print('Always look on the bright side of life')

#Escape character to include special characters
#print('Brian's mother: He's not an angel. He's a very naughty boy!')
print('Brian\'s mother: He\'s not an angel. He\'s a very naughty boy!')

#Newline character
print('One \nTwo \nThree')

#Triple quotes-Newlines will be automatically added for strings that are created using three quotes
print("""this
is a
multiline
text with double quote""")

print('''this
is a
multiline
text with single quote''')

#space character
print('One\tTwo\tThree')


